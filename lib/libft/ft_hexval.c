char	ft_hexval(char c)
{
	if (c >= '0' && c <= '9')
		return (c - 060);
	else if (c >= 'a' && c <= 'f')
		return (c - 'a' + 10);
	else if (c >= 'A' && c <= 'F')
		return (c - 'A' + 10);
	else
		return (0);
}
